# MIN(FMT/DP)>5 is minimum depth of 5.
# MAX(MFT/DP)<200 is a maximum depth of 200.
# %QUAL>20 filters for quality above 20. 

bcftools view /data/Mpileup_BCFs/OFK_Concat_18Oct2019.vcf -v snps -m 2 -M 2 -q 0.05:minor -i 'MIN(FMT/DP)>5 & MAX(FMT/DP)<200 & %QUAL>20' -o /data/Mpileup_BCFs/OFK_Concat_18Oct2019_BCFTools_Biallelic_SNPsOnly_MAF0.05_MINDP5_MAXDP200_Q20.vcf

